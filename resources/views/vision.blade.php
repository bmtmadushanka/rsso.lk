                            <p>
                            <b>Many people in Sri Lanka come into contact with social services at some point in their lives. When they do they find valuable services provided by a wide range of individuals and organisations. They experience dedicated and skilled workers who are there to protect and support people or help them to deal with challenging circumstances and to get back on track with their lives.</b>


The life-changing work undertaken by social services workers, on behalf of us all, to support children, adults and families to live lives in which they are able to achieve their potential is extremely valuable work – contributing to a more equal and socially just Sri Lanka. The staff in social services are dedicated and committed to their work and this strategy recognises and supports both their unique contribution and the contributions they make in strong partnerships with others, such as people in the health workforce, to supporting our communities.....


The Changing Lives report, a decade ago, set in train a wide range of developments which has led to on-going reform and improvement across social services, for example the introduction of a statutory basis for self-directed support. Changing Lives sought “to equip social work services to rise to the challenge of supporting and protecting our most vulnerable people and communities in the early part of the 21st century”. It is clear that a great deal has been achieved over the last ten years and Sri Lanka has social services that are highly valued and supporting people to have better lives. The landscape and context has, however, also changed significantly during that period and new challenges have arisen: financial constraints and austerity; increasing expectations of services and for services to be delivered in new ways; issues arising from the model of delivery and complexities in the market approach to care; challenges in approaches to commissioning and procurement; changes to the welfare system, medical advances and changes in the demography of Sri Lanka. The progress being made towards delivery of integration of adult health and social care is a key example of a number of recent significant developments intended to deal with the challenges and improve the outcomes for people who use services. Transformational change of this scale is in itself not straightforward and brings its own complexity and challenge for people in the workforce.

The Raigampura social servises Organization partners and their wider set of stakeholders recognise that many of the current challenges are difficult issues for which solutions will not be easy to find and that the system and culture change required are in themselves not simple. However they are all committed to working in partnership towards ensuring delivery of the vision for social services and to doing everything they can to mitigate and manage the impacts of some of the current challenges.


The purpose of this strategy is to ensure that social services are able to play their full part as robust and strong partners within the overall landscape of Sri Lankan’s public services and the shared outcomes they are working to achieve. This strategy summarises the wide range of current developments that are already underway to address the challenges. It also outlines a number of new actions intended to further support delivery of the vision. An implementation plan will be developed to address how and by when the actions will be taken forward.

The actions in this plan belong to a wide range of  Social Services  in addition to the Raigampura Social Services Organization  partners and there will be wider involvement as the Implementation Plan is developed and progressed.

